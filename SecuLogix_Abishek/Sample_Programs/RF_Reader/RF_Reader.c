// Draw one character at pixel column `col`, page row `page` (0-7)
static void oled_char(uint8_t col, uint8_t page, char c) {
    if (c < 0x20 || c > 0x7E) c = ' ';
    const uint8_t *g = font5x7[c - 0x20];
    for (uint8_t i = 0; i < 5; i++) {
        uint16_t idx = (uint16_t)page * OLED_W + col + i;
        if (idx < sizeof(oled_buf)) oled_buf[idx] = g[i];
    }
    uint16_t idx = (uint16_t)page * OLED_W + col + 5;
    if (idx < sizeof(oled_buf)) oled_buf[idx] = 0x00;
}

// Draw string at column `col`, page row `page`
static void oled_text(uint8_t col, uint8_t page, const char *s) {
    while (*s && col < OLED_W) {
        oled_char(col, page, *s++);
        col += 6;
    }
}

// ── Wiegand state ─────────────────────────────────────────────────────────

static volatile uint32_t wg_data    = 0;
static volatile uint8_t  wg_count   = 0;
static volatile bool     wg_ready   = false;
static volatile uint32_t wg_last_ms = 0;

static void wg_callback(uint gpio, uint32_t events) {
    if (!(events & GPIO_IRQ_EDGE_FALL)) return;
    wg_data <<= 1;
    if (gpio == WG_D1_PIN) wg_data |= 1u;  // D1 = 1 bit, D0 = 0 bit
    wg_last_ms = to_ms_since_boot(get_absolute_time());
    if (++wg_count >= WIEGAND_BITS) {
        wg_ready = true;
        wg_count = 0;
    }
}

// ── Output helpers ────────────────────────────────────────────────────────

static void send_data(uint32_t raw) {
    char line1[22], line2[22];
    snprintf(line1, sizeof(line1), "DEC:%-10" PRIu32, raw);
    snprintf(line2, sizeof(line2), "HEX:0x%08" PRIX32, raw);

    // UART + USB
    char buf[64];
    snprintf(buf, sizeof(buf), "%s  %s\r\n", line1, line2);
    uart_puts(UART_ID, buf);
    printf("%s", buf);

    // OLED: line1 on page 2, line2 on page 4
    oled_clear();
    oled_text(0, 0, "== Wiegand ID ==");
    oled_text(0, 2, line1);
    oled_text(0, 4, line2);
    oled_flush();
}

// ── Main ──────────────────────────────────────────────────────────────────

int main() {
    stdio_init_all();

    // UART0
    uart_init(UART_ID, BAUD_RATE);
    gpio_set_function(UART_TX_PIN, GPIO_FUNC_UART);
    gpio_set_function(UART_RX_PIN, GPIO_FUNC_UART);

    // I2C0 for OLED
    i2c_init(I2C_PORT, 400 * 1000);
    gpio_set_function(I2C_SDA_PIN, GPIO_FUNC_I2C);
    gpio_set_function(I2C_SCL_PIN, GPIO_FUNC_I2C);
    gpio_pull_up(I2C_SDA_PIN);
    gpio_pull_up(I2C_SCL_PIN);
    oled_init();
    oled_clear();
    oled_text(0, 0, "== Wiegand ID ==");
    oled_text(0, 3, "  Waiting...");
    oled_flush();

    // Wiegand inputs
    gpio_init(WG_D0_PIN);
    gpio_set_dir(WG_D0_PIN, GPIO_IN);
    gpio_pull_up(WG_D0_PIN);

    gpio_init(WG_D1_PIN);
    gpio_set_dir(WG_D1_PIN, GPIO_IN);
    gpio_pull_up(WG_D1_PIN);

    gpio_set_irq_enabled_with_callback(WG_D0_PIN, GPIO_IRQ_EDGE_FALL, true, &wg_callback);
    gpio_set_irq_enabled(WG_D1_PIN, GPIO_IRQ_EDGE_FALL, true);

    uart_puts(UART_ID, "Wiegand Reader Ready...\r\n");
    printf("Wiegand Reader Ready...\r\n");

    while (true) {
        // USB serial → UART0
        int ch = getchar_timeout_us(0);
        if (ch != PICO_ERROR_TIMEOUT) {
            uart_putc(UART_ID, (char)ch);
            uart_putc(UART_ID, '\r');
            uart_putc(UART_ID, '\n');
        }

        // Reset partial frame on timeout
        if (wg_count > 0) {
            uint32_t now = to_ms_since_boot(get_absolute_time());
            if ((now - wg_last_ms) > WIEGAND_TIMEOUT_MS) {
                wg_data  = 0;
                wg_count = 0;
            }
        }

        // Received a full frame
        if (wg_ready) {
            send_data(wg_data);
            wg_data  = 0;
            wg_ready = false;
        }

        sleep_ms(1);
    }
}
