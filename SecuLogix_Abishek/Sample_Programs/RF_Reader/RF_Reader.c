// ── Wiegand pins ──────────────────────────────────────────────────────────
#define WG_D0_PIN          2
#define WG_D1_PIN          3
#define WIEGAND_BITS       32
#define WIEGAND_TIMEOUT_MS 50

// ── UART ──────────────────────────────────────────────────────────────────
#define UART_ID     uart0
#define BAUD_RATE   115200
#define UART_TX_PIN 0       // GP0
#define UART_RX_PIN 1       // GP1

// ── I2C / SSD1306 ─────────────────────────────────────────────────────────
#define I2C_PORT     i2c0
#define I2C_SDA_PIN  4      // GP4 //r6p
#define I2C_SCL_PIN  5      // GP5 r7p
#define OLED_ADDR    0x3C   // 7-bit (your display is 0x78 >> 1 = 0x3C)
#define OLED_W       128
#define OLED_H       64

// ── SSD1306 minimal driver ────────────────────────────────────────────────

static uint8_t oled_buf[OLED_W * OLED_H / 8];  // 1024-byte framebuffer

static void oled_cmd(uint8_t cmd) {
    uint8_t buf[2] = {0x00, cmd};
    i2c_write_blocking(I2C_PORT, OLED_ADDR, buf, 2, false);
}

static void oled_init(void) {
    sleep_ms(100);
    static const uint8_t seq[] = {
        0xAE,               // display off
        0xD5, 0x80,         // clock div ratio
        0xA8, 0x3F,         // multiplex ratio 64
        0xD3, 0x00,         // display offset 0
        0x40,               // start line 0
        0x8D, 0x14,         // charge pump on
        0x20, 0x00,         // horizontal addressing mode
        0xA1,               // segment remap
        0xC8,               // COM scan direction remapped
        0xDA, 0x12,         // COM pins config
        0x81, 0xCF,         // contrast
        0xD9, 0xF1,         // pre-charge period
        0xDB, 0x40,         // VCOMH deselect level
        0xA4,               // display from RAM
        0xA6,               // normal (not inverted)
        0xAF,               // display on
    };
    for (size_t i = 0; i < sizeof(seq); i++) oled_cmd(seq[i]);
}

static void oled_flush(void) {
    oled_cmd(0x21); oled_cmd(0); oled_cmd(127); // col range 0-127
    oled_cmd(0x22); oled_cmd(0); oled_cmd(7);   // page range 0-7
    uint8_t stream[1 + sizeof(oled_buf)];
    stream[0] = 0x40;
    memcpy(&stream[1], oled_buf, sizeof(oled_buf));
    i2c_write_blocking(I2C_PORT, OLED_ADDR, stream, sizeof(stream), false);
}

static void oled_clear(void) {
    memset(oled_buf, 0, sizeof(oled_buf));
}

